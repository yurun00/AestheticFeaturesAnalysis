function[huedif, lumdif, satdif, huetemdif, weightdif]=Contrast(rgbimg)
C = makecform('srgb2lab');
labimg = applycform((rgbimg),C);
C = makecform('lab2lch');
lchimg = applycform(lab2double(labimg),C);
[hue,tempscore] = ColorTemperature(rgbimg);
[weightscore] = ColorWeight(rgbimg);
luminance = lchimg(:,:,1);
saturation = lchimg(:,:,2);
hue = lchimg(:,:,3);
height = size(rgbimg,1);
width = size(rgbimg,2);
lumdif = mean(mean(sqrt(abs(luminance.*luminance - mean(mean(luminance)).^2))));
satdif = mean(mean(sqrt(abs(saturation.*saturation - mean(mean(saturation)).^2))));
huedif = mean(mean(sqrt(abs(hue.*hue - mean(mean(hue)).^2))));
huetemdif = mean(mean(sqrt(abs(tempscore.*tempscore - mean(mean(tempscore)).^2))));
weightdif = mean(mean(sqrt(abs(weightscore.*weightscore - mean(mean(weightscore)).^2))));
lumdif = lumdif/100;
satdif = satdif/100;
huedif = huedif/360;
huetemdif = huetemdif/1;
weightdif = weightdif/1;
